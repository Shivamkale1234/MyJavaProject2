package hefshineSir;

public class If_EvenOdd {

	public static void main(String[] args) {

        int num = 99;
        
        if(num%2==0)
        {
        	System.out.println(num + " is the even number");
        }
        
        if(num%2!=0)
        {
        	System.out.println(num + " is the odd number");
        }
        
	}
}
