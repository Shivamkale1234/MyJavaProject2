package loops;

// at least one time code will be executed in this 
// loop statement then another check the condition.

public class dowhileloop_Printnumfrom10to1
{
    public static void main(String[] args) 
    {
    	
        int i=10;
        
        do 
        {
            System.out.println(i);
            i--;
            
        }while(i>=1);
    }
}